let num1 = 5;
let num2 = 8;

console.log("num1 == num2:", num1 == num2);   
console.log("num1 === num2:", num1 === num2); 
console.log("num1 != num2:", num1 != num2);   
console.log("num1 !== num2:", num1 !== num2); 
console.log("num1 > num2:", num1 > num2);    
console.log("num1 < num2:", num1 < num2);     
console.log("num1 >= num2:", num1 >= num2);   
console.log("num1 <= num2:", num1 <= num2);   

let numero = 5;
let cadena = "5";
console.log("5 == '5':", numero == cadena);   
console.log("5 === '5':", numero === cadena); 

 // La diferencia entre == y === es que == realiza una comparación de valor, lo que significa que convierte los operandos a un tipo común antes de compararlos. Por ejemplo, 5 == "5" es true porque el número 5 se convierte a la cadena "5" antes de la comparación.
